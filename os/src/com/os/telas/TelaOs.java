    private void pesquisar_cliente(){
        String sql = "select idcli as ID, nomeCli as Nome, foneCli as Fone from tbClientes where nomeCli like ?";
        
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtCliPesquisar.getText()+"%");
            rs=pst.executeQuery();
            
            tblClientes.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void setar_campos(){
        int setar = tblClientes.getSelectedRow();
        txtCliId.setText(tblClientes.getModel().getValueAt(setar, 0).toString());
    }
    
    private void emitir_os(){
        String sql = "insert into tbOs(situacao, equipamento, defeito, servico, tecnico, valor, idCli) values(?,?,?,?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtOsSit.getText());
            pst.setString(2, txtOsEquip.getText());
            pst.setString(3, txtOsDef.getText());
            pst.setString(4, txtOsServ.getText());
            pst.setString(5, txtOsTec.getText());
            pst.setString(6, txtOsValor.getText().replace(",", "."));
            pst.setString(7, txtCliId.getText());
            
            if ((txtCliId.getText().isEmpty()||txtOsServ.getText().isEmpty()||txtOsSit.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios");
            } else {
                int adicionado = pst.executeUpdate();
                if(adicionado>0){
                    JOptionPane.showMessageDialog(null, "OS emitida com sucesso.");
                    txtCliId.setText(null);
                    txtOsEquip.setText(null);
                    txtOsDef.setText(null);
                    txtOsServ.setText(null);
                    txtOsTec.setText(null);
                    txtOsValor.setText(null);
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void consultar(){
        
        String sql = "select * from tbOs where os=?";
        
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtOs.getText());
            rs=pst.executeQuery();
            
            if (rs.next()) {
                txtData.setText(rs.getString(2));
                txtOsSit.setText(rs.getString(4));
                txtOsEquip.setText(rs.getString(5));
                txtOsDef.setText(rs.getString(6));
                txtOsServ.setText(rs.getString(7));
                txtOsTec.setText(rs.getString(8));
                txtOsValor.setText(rs.getString(9));
            } else {
                JOptionPane.showMessageDialog(null, "OS não cadastrada");
                txtData.setText(null);
                txtOsEquip.setText(null);
                txtOsDef.setText(null);
                txtOsServ.setText(null);
                txtOsTec.setText(null);
                txtOsValor.setText(null);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void alterar(){
        
        String sql = "update tbOs set equipamento=?, defeito=?, servico=?, tecnico=?, valor=100 where os=?";
        
        try {
            pst=conexao.prepareStatement(sql);
            pst.setString(1, txtOsEquip.getText());
            pst.setString(2, txtOsDef.getText());
            pst.setString(3, txtOsServ.getText());
            pst.setString(4, txtOsTec.getText());
            pst.setString(5, txtOsValor.getText());
            pst.setString(5, txtOs.getText());
            
            if ((txtOsEquip.getText().isEmpty()||txtOsDef.getText().isEmpty())) {

                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios");

            } else {
                
                int adicionado = pst.executeUpdate();
                
                if(adicionado>0){
                    JOptionPane.showMessageDialog(null, "OS alterada!");
                    txtOsEquip.setText(null);
                    txtOsDef.setText(null);
                    txtOsServ.setText(null);
                    txtOsTec.setText(null);
                    txtOsValor.setText(null);
                }
                
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void remover(){
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir essa OS?");
        if(confirma==JOptionPane.YES_OPTION){
            String sql = "delete from tbOs where os=?";
            try {
                pst=conexao.prepareStatement(sql);
                pst.setString(1, txtOs.getText());
                int apagado = pst.executeUpdate();
                if(apagado>0){
                    JOptionPane.showMessageDialog(null, "OS excluída");
                    txtOsEquip.setText(null);
                    txtOsDef.setText(null);
                    txtOsServ.setText(null);
                    txtOsTec.setText(null);
                    txtOsValor.setText(null);
                    
                    btnOsAdicionar.setEnabled(true);
                }
            } catch (Exception e) {
                
            }
        }
    }
        
